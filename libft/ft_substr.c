#include "libft.h"

char	*ft_substr(char const *s, unsigned int start,
					size_t len, t_list *alloc_list)
{
	char	*ptr;
	size_t	i;
	size_t	s_len;

	if (s == NULL)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return ((char *)ft_calloc(1, sizeof(char), alloc_list));
	if (len > s_len - start)
		len = s_len - start;
	ptr = (char *)ft_malloc((len + 1), &alloc_list);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		ptr[i] = s[start + i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
